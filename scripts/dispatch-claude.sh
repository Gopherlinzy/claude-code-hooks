#!/usr/bin/env bash
# FAIL_MODE=open — hook 自身故障时静默放行，不阻塞 Claude Code
# dispatch-claude.sh — Claude Code 任务派发封装脚本
# 安全约束：Security best practices
#
# 用法：
#   dispatch-claude.sh -p "prompt" [-n task_name] [-w workdir] [--sync]
#
# 参数：
#   -p | --prompt   (必需) 传递给 claude --print 的任务 prompt
#   -n | --name     任务名称，默认 "unnamed"
#   -w | --workdir  工作目录，默认当前目录
#   --sync          同步前台模式（阻塞直到任务完成，适合调试）
#   --timeout N     异步模式超时秒数，默认 1800（30分钟）
#   --stream        启用 stream-json 输出模式（自动追加 --output-format stream-json --verbose）

set -euo pipefail

# === JSONL 审计日志函数（自身 fail-safe，绝不抛错）===
_log_jsonl() {
    local _jsonl_dir="${HOME}/.openclaw/logs"
    local _jsonl_file="${_jsonl_dir}/hooks-audit.jsonl"
    mkdir -p "${_jsonl_dir}" 2>/dev/null || true
    printf '%s\n' "$1" >> "${_jsonl_file}" 2>/dev/null || true
}

# === 环境白名单隔离函数 (P1 安全加固) ===
sanitize_env() {
    local CLEAN_ENV=(PATH HOME USER SHELL TERM LANG LC_ALL ANTHROPIC_API_KEY CLAUDE_TASK_ID CLAUDE_TASK_NAME TMPDIR)
    for var in $(compgen -e); do
        if [[ ! " ${CLEAN_ENV[*]} " =~ " ${var} " ]]; then
            unset "$var"
        fi
    done
    # 显式 unset 高危变量（双重保险）
    unset HISTFILE SSH_AUTH_SOCK SSH_AGENT_PID AWS_ACCESS_KEY_ID AWS_SECRET_ACCESS_KEY OPENAI_API_KEY 2>/dev/null || true
}

# === Skills 索引加载（Iris P1：禁止拼接字符串，用数组传参） ===
CLAUDE_EXTRA_ARGS=()
_SKILL_INDEX_FILE="${HOME}/.openclaw/cache/skills-index.md"
_SKILL_INDEX_TMP=""  # ASYNC 模式用的临时文件路径

# 调用 generate-skill-index.sh（lazy 缓存，无变更时为 no-op）
_SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
if [[ -x "${_SCRIPT_DIR}/generate-skill-index.sh" ]]; then
    "${_SCRIPT_DIR}/generate-skill-index.sh" 2>/dev/null || true
fi

# 若索引存在，加入 --append-system-prompt 参数（数组方式）
if [[ -s "${_SKILL_INDEX_FILE}" ]]; then
    CLAUDE_EXTRA_ARGS=("--append-system-prompt" "$(< "${_SKILL_INDEX_FILE}")")
fi

# === 参数默认值 ===
PROMPT=""
TASK_NAME="unnamed"
WORKDIR="$(pwd)"
SYNC_MODE=false
TIMEOUT_SECS=1800
STREAM_MODE=false

# === 参数解析 ===
while [[ $# -gt 0 ]]; do
    case "$1" in
        -p|--prompt)
            PROMPT="$2"
            shift 2
            ;;
        -n|--name)
            TASK_NAME="$2"
            shift 2
            ;;
        -w|--workdir)
            WORKDIR="$2"
            shift 2
            ;;
        --sync)
            SYNC_MODE=true
            shift
            ;;
        --timeout)
            TIMEOUT_SECS="$2"
            shift 2
            ;;
        --stream)
            STREAM_MODE=true
            shift
            ;;
        *)
            echo "[dispatch-claude] ERROR: unknown argument: $1" >&2
            exit 1
            ;;
    esac
done

# === 校验必需参数 ===
# === stream-json 模式参数注入 ===
if [ "${STREAM_MODE}" = true ]; then
    CLAUDE_EXTRA_ARGS+=("--output-format" "stream-json" "--verbose")
fi

if [ -z "${PROMPT}" ]; then
    echo "[dispatch-claude] ERROR: -p/--prompt is required" >&2
    exit 1
fi

# === 进度追踪文档注入（Claude Code 黑箱破解机制） ===
# 在 prompt 末尾自动追加进度文档维护指令
# 即使子代理超时，外部可通过 cat {workdir}/.claude-progress.md 获取实时进度
_PROGRESS_SUFFIX='

【第一强制动作 — 立即执行】
在执行任何其他操作前，你的第一个强制动作是立即创建 .claude-progress.md 文件。即使只写入标题"# Task Progress"和"## 当前状态: 🔄 初始化中"也必须先创建此文件。这必须是你的第一个 tool call，不可跳过。

【任务拆解与进度追踪 — 强制指令】
创建 .claude-progress.md 后，按以下格式将任务拆解为 2-5 分钟可完成的微型原子步骤：

# Task Progress

## 文件映射
列出此任务涉及的所有文件变更：
- Create: （新建文件路径）
- Modify: （修改文件路径）
- Test: （测试文件路径）

## 微型步骤清单
要求：每个步骤必须是 2-5 分钟可独立完成和验证的原子操作。
- [ ] Step 1: [具体描述]
- [ ] Step 2: [具体描述]
...

## 当前状态: 🔄 进行中
## 开始时间: （写入当前时间）

每完成一个步骤：
1. 将该步骤的 [ ] 改为 [x]
2. 执行对应的验证命令（如 test/lint/build/grep）并将结果记录到 .claude-progress.md 该步骤下方
3. 若当前目录为 git 仓库，执行 git add -A && git commit -m "[TaskID] Step N: 简述" 提交该原子变更
4. 更新"当前状态"行
遇到错误时，在 ## Errors 段落记录错误信息和尝试的修复方案。
全部完成后，将状态更新为 ✅ 完成，并在 ## Summary 段落写入变更清单。'

PROMPT="${PROMPT}${_PROGRESS_SUFFIX}"

# === 确保 workdir 存在 ===
mkdir -p "${WORKDIR}"

# === 生成 CLAUDE_TASK_ID（date +%s_$$）===
# 格式：unix秒_当前PID，确保跨进程唯一性
export CLAUDE_TASK_ID="$(date +%s)_$$"
export CLAUDE_TASK_NAME="${TASK_NAME}"

# === 确保日志输出目录存在 ===
LOG_DIR="/tmp/openclaw-hooks"
mkdir -p "${LOG_DIR}"
LOG_FILE="${LOG_DIR}/${CLAUDE_TASK_ID}.log"

# === 写入 task-meta.json ===
META_FILE="${WORKDIR}/task-meta.json"
START_TIME="$(date -u +"%Y-%m-%dT%H:%M:%SZ")"

# 先写占位 PID=0，异步模式下启动后再更新
cat > "${META_FILE}" <<EOF
{
  "task_id": "${CLAUDE_TASK_ID}",
  "name": "${TASK_NAME}",
  "prompt": $(echo "${PROMPT}" | python3 -c 'import json,sys; print(json.dumps(sys.stdin.read().rstrip()))'),
  "workdir": "${WORKDIR}",
  "start_time": "${START_TIME}",
  "pid": 0,
  "sync_mode": ${SYNC_MODE}
}
EOF

# === 自动生成 .claudeignore（若不存在） ===
if [ ! -f "${WORKDIR}/.claudeignore" ]; then
    cat > "${WORKDIR}/.claudeignore" <<'CLAUDEIGNORE'
# Auto-generated by dispatch-claude.sh — 通用噪音过滤
node_modules/
dist/
vendor/
.git/
*.min.js
*.min.css
*_gen.go
*.pb.go
*.pb.validate.go
__pycache__/
.next/
build/
coverage/
CLAUDEIGNORE
    echo "[dispatch-claude] Auto-generated .claudeignore in ${WORKDIR}"
fi

# === 执行模式选择 ===
if [ "${SYNC_MODE}" = true ]; then
    # --sync：前台阻塞执行（等价于现有 exec 阻塞模式，适合调试和 Fallback）
    echo "[dispatch-claude] SYNC mode | task_id=${CLAUDE_TASK_ID} | name=${TASK_NAME}"
    if command -v jq &>/dev/null; then
        _log_jsonl "$(jq -nc --arg ts "$(date -Iseconds)" --arg hook "dispatch-claude" --arg tid "${CLAUDE_TASK_ID}" --arg name "${TASK_NAME}" --arg mode "sync" --arg wd "${WORKDIR}" '{ts:$ts,hook:$hook,task_id:$tid,name:$name,mode:$mode,workdir:$wd}')"
    else
        _log_jsonl "{\"ts\":\"$(date -Iseconds)\",\"hook\":\"dispatch-claude\",\"task_id\":\"${CLAUDE_TASK_ID}\",\"name\":\"${TASK_NAME}\",\"mode\":\"sync\",\"workdir\":\"${WORKDIR}\"}"
    fi
    source ~/.zshrc 2>/dev/null || true
    sanitize_env
    cd "${WORKDIR}"
    claude --print --permission-mode bypassPermissions "${CLAUDE_EXTRA_ARGS[@]}" "${PROMPT}"
else
    # 默认异步模式：nohup + disown，防止 SIGHUP 级联终止
    source ~/.zshrc 2>/dev/null || true
    export -f sanitize_env

    # ASYNC 模式：将索引内容和 PROMPT 写入临时文件，避免 nohup bash -c 内嵌套引号地狱
    _SKILL_INDEX_TMP=""
    _ASYNC_SKILL_ARG=""
    if [[ -s "${_SKILL_INDEX_FILE}" ]]; then
        _SKILL_INDEX_TMP="$(mktemp /tmp/openclaw-skill-index.XXXXXX)"
        cp "${_SKILL_INDEX_FILE}" "${_SKILL_INDEX_TMP}"
        _ASYNC_SKILL_ARG="--append-system-prompt \"\$(<'${_SKILL_INDEX_TMP}')\""
    fi

    # PROMPT 写入临时文件，彻底避免引号转义问题
    _PROMPT_TMP="$(mktemp /tmp/openclaw-prompt.XXXXXX)"
    printf '%s' "${PROMPT}" > "${_PROMPT_TMP}"

    # stream-json 模式参数（异步模式下通过字符串拼接传入 nohup）
    _ASYNC_STREAM_ARG=""
    if [ "${STREAM_MODE}" = true ]; then
        _ASYNC_STREAM_ARG="--output-format stream-json --verbose"
    fi

    nohup bash -c "
        # 清理临时文件的 EXIT trap（同时清理 _SKILL_INDEX_TMP 和 _PROMPT_TMP）
        trap 'rm -f \"${_PROMPT_TMP}\" \"${_SKILL_INDEX_TMP}\"' EXIT
        cd '${WORKDIR}'
        export CLAUDE_TASK_ID='${CLAUDE_TASK_ID}'
        source ~/.zshrc 2>/dev/null || true
        sanitize_env
        timeout ${TIMEOUT_SECS}s claude --print --permission-mode bypassPermissions ${_ASYNC_SKILL_ARG} ${_ASYNC_STREAM_ARG} \"\$(cat '${_PROMPT_TMP}')\" >> '${LOG_FILE}' 2>&1
    " > /dev/null 2>&1 &
    BG_PID=$!
    disown ${BG_PID}

    # === 写入 .meta 文件供 reap-orphans.sh 追踪 ===
    META_REAP="${LOG_DIR}/${CLAUDE_TASK_ID}.meta"
    cat > "${META_REAP}" <<METAEOF
{
  "task_id": "${CLAUDE_TASK_ID}",
  "name": "${TASK_NAME}",
  "pid": ${BG_PID},
  "start_epoch": $(date +%s),
  "workdir": "${WORKDIR}"
}
METAEOF

    # === L0 启动确认信号：进程已拉起 ===
    cat > "${WORKDIR}/.claude-dispatched" <<SIGEOF
{
  "task_id": "${CLAUDE_TASK_ID}",
  "pid": ${BG_PID},
  "dispatched_at": "$(date -u +"%Y-%m-%dT%H:%M:%SZ")",
  "workdir": "${WORKDIR}"
}
SIGEOF

    # 将实际 PID 更新回 task-meta.json
    python3 - <<PYEOF
import json, os

meta_path = "${META_FILE}"
try:
    with open(meta_path, "r") as f:
        meta = json.load(f)
    meta["pid"] = ${BG_PID}
    with open(meta_path, "w") as f:
        json.dump(meta, f, indent=2, ensure_ascii=False)
except Exception as e:
    print(f"[dispatch-claude] WARNING: failed to update PID in task-meta.json: {e}")
PYEOF

    echo "[dispatch-claude] ASYNC mode | task_id=${CLAUDE_TASK_ID} | name=${TASK_NAME} | pid=${BG_PID} | log=${LOG_FILE}"
    if command -v jq &>/dev/null; then
        _log_jsonl "$(jq -nc --arg ts "$(date -Iseconds)" --arg hook "dispatch-claude" --arg tid "${CLAUDE_TASK_ID}" --arg name "${TASK_NAME}" --arg mode "async" --arg pid "${BG_PID}" --arg wd "${WORKDIR}" '{ts:$ts,hook:$hook,task_id:$tid,name:$name,mode:$mode,pid:$pid,workdir:$wd}')"
    else
        _log_jsonl "{\"ts\":\"$(date -Iseconds)\",\"hook\":\"dispatch-claude\",\"task_id\":\"${CLAUDE_TASK_ID}\",\"name\":\"${TASK_NAME}\",\"mode\":\"async\",\"pid\":\"${BG_PID}\",\"workdir\":\"${WORKDIR}\"}"
    fi
fi

# 输出 task_id 到 stdout 供外部追踪
echo "${CLAUDE_TASK_ID}"
